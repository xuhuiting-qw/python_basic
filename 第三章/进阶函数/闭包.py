
# 闭包------闭包 = 内层函数 + 被内层函数所引用的外层变量
# 闭包形成条件：
# 1、要有函数嵌套
# 2、在 内层函数 中 ，要访问外层函数的 变量
# 3、并且外层函数 要返回内层函数--------只有返回了内层函数，闭包才能“活下来”

def outer():
    num = 10

    def inner():
        nonlocal num
        num += 1
        print(num)
    return inner
f = outer()

# 结论 ：
# 1、outer函数中，被inner所使用到的那些变量，会被封存在【闭包单元（cell）】中
# 2、这些cell会组成一个__closure__ 元组，最终放在inner函数身上

# 打印 __closure__ 元组
print(f.__closure__)
# 打印 __closure__ 元组的某一项的具体值
print(f.__closure__[0].cell_contents)

# 注意点：
# 1、调用n次外层函数，就会得到n个不同的闭包，且这些闭包之间互不影响
# 2、内层函数中用到的外层变量是可变对象，多个闭包之间依然互不影响

# 优点：
# 1、可以“记住”状态：不用全局变量，也不用写类，就能在多次调用之间保存数据
# 2、可以做“配置过的函数”：先传一部分参数，把环境固定住，得到一个定制版函数
# 3、可以实现简单的“数据隐藏”：外层变量对外不可见，只能通过内层函数访问
# 4、是装饰器(decorator) 等高级用法的基础
def beauty(char,n):
    def show_msg(msg):
        print(char * n + msg + char * n)
    return show_msg
# 此写法就是一种定制版的函数
f1 = beauty('*',5)
f1('你好！')

# 缺点
# 1、理解成本较高：对初学者不友好，滥用会让代码难读
# 2、如果闭包里引用了很大的对象，又长期不释放，可能会增加内存占用
# 3、很多场景下，其实用 类 + 实例属性  会更清晰，闭包不一定是最优解

